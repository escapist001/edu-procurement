def make_session() -> requests.Session:
    s = requests.Session()
    s.headers.update({
        "User-Agent": UA,
        "Accept-Language": "ru-RU,ru;q=0.9",
        "Accept": "text/html,application/xhtml+xml",
    })
    return s


def get(session: requests.Session, url: str, *, params=None,
        retries: int = 3, pause: float = 0.6, timeout: int = 45) -> requests.Response | None:
    """GET с повторами и вежливой паузой. Возвращает Response или None (после N неудач).

    Портал ЕИС ограничивает частоту (HTTP 429). При 429 ждём заметно дольше: короткий
    повтор бесполезен — нужен «остыть». Возврат None означает сетевой сбой/бан (а НЕ
    «данных нет») — вызывающий код должен трактовать это как «повторить позже».
    """
    for attempt in range(1, retries + 1):
        try:
            r = session.get(url, params=params, timeout=timeout, verify=False,
                            allow_redirects=True)
            if r.status_code == 429:
                wait = 15 * attempt
                print(f"    ! 429 rate-limit — остываем {wait}с", flush=True)
                time.sleep(wait)
                continue
            r.raise_for_status()
            time.sleep(pause)
            return r
        except Exception as exc:                       # noqa: BLE001 — сеть капризна
            wait = pause * attempt * 2
            time.sleep(wait)
    return None


