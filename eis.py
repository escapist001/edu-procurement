# --------------------------------------------------------------------------- цена
def parse_price(text: str):
    """'502 416,84 ₽' -> 502416.84 (или None)."""
    if not text:
        return None
    t = (text.replace("\xa0", "").replace(" ", "")
             .replace("₽", "").replace("руб.", "").replace(",", "."))
    m = re.search(r"\d+(\.\d+)?", t)
    return float(m.group()) if m else None


