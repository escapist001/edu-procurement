def region_from_inn(inn: str) -> str | None:
    """Первые 2 цифры ИНН юрлица = код субъекта РФ по справочнику ФНС.

    Внимание: это работает ТОЛЬКО для ИНН. Коды ОКТМО устроены иначе (другой
    справочник), применять к ним эту таблицу нельзя.
    """
    if not inn or len(inn) < 2 or not inn[:2].isdigit():
        return None
    key = REGION_CANON.get(inn[:2], inn[:2])
    return REGION_BY_CODE.get(key)


# Явное место нахождения из карточки организации — самый осмысленный «регион закупки»
# (фактический субъект деятельности, а не регион налоговой регистрации по ИНН). В адресе
# субъект стоит в канонической форме, поэтому матчим его по «ядру» названия.
_CANON = sorted(set(REGION_BY_CODE.values()))


def _norm(s: str) -> str:
    s = s.lower().replace("ё", "е")
    for junk in ("автономный округ", "автономная область", "область", " обл",
                 " край", "республика", " респ", " ао", " аобл",
                 "  ", " г ", " город", "-"):
        s = s.replace(junk, " ")
    return " ".join(s.split())


