def region_from_address(addr: str) -> str | None:
    """'РФ, 640003, Курганская обл, Курган г, ...' -> 'Курганская область'."""
    if not addr:
        return None
    a = _norm(addr)
    for core in _CORES:
        if core and core in a:
            return _CORE_TO_CANON[core]
    return None
